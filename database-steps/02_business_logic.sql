-- Materialized view for compliment statistics (refresh periodically)
CREATE MATERIALIZED VIEW "mv_compliment_stats" AS
SELECT 
  cv.code as company_value_code,
  cv.name as company_value_name,
  COUNT(*) as total_compliments,
  AVG(c.quantity) as avg_coins,
  SUM(c.quantity) as total_coins,
  COUNT(DISTINCT c.id_sender_user) as unique_senders,
  COUNT(DISTINCT c.id_receiver_user) as unique_receivers,
  DATE_TRUNC('month', c.created_at) as month_year
FROM "compliment" c
JOIN "company_values" cv ON c.company_value_id = cv.id
WHERE c.created_at >= NOW() - INTERVAL '12 months'
GROUP BY cv.code, cv.name, DATE_TRUNC('month', c.created_at)
ORDER BY month_year DESC, total_compliments DESC;

-- Index for materialized view
CREATE INDEX "idx_mv_compliment_stats_month" ON "mv_compliment_stats" ("month_year");
CREATE INDEX "idx_mv_compliment_stats_value" ON "mv_compliment_stats" ("company_value_code");

