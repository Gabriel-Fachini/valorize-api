-- Ensure category hierarchy doesn't create cycles
CREATE OR REPLACE FUNCTION check_category_hierarchy()
RETURNS TRIGGER AS $$
DECLARE
  current_id INT;
  max_depth INT := 10;
  depth INT := 0;
BEGIN
  IF NEW.parent_id IS NULL THEN
    RETURN NEW;
  END IF;
  
  current_id := NEW.parent_id;
  
  WHILE current_id IS NOT NULL AND depth < max_depth LOOP
    IF current_id = NEW.id THEN
      RAISE EXCEPTION 'Category hierarchy cycle detected';
    END IF;
    
    SELECT parent_id INTO current_id 
    FROM reward_categories 
    WHERE id = current_id;
    
    depth := depth + 1;
  END LOOP;
  
  IF depth >= max_depth THEN
    RAISE EXCEPTION 'Category hierarchy too deep (max 10 levels)';
  END IF;
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER check_category_hierarchy_trigger
  BEFORE INSERT OR UPDATE ON "reward_categories"
  FOR EACH ROW
  EXECUTE FUNCTION check_category_hierarchy();

