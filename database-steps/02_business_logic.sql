-- User dashboard data view (frequently accessed data)
CREATE VIEW "v_user_dashboard" AS
SELECT 
  u.id,
  u.name,
  u.email,
  u.coins_to_donate,
  u.coins_to_spend,
  -- Compliment stats
  (SELECT COUNT(*) FROM compliment WHERE id_receiver_user = u.id) as compliments_received,
  (SELECT COUNT(*) FROM compliment WHERE id_sender_user = u.id) as compliments_sent,
  (SELECT SUM(quantity) FROM compliment WHERE id_receiver_user = u.id) as total_coins_received,
  -- Recent activity
  (SELECT MAX(created_at) FROM compliment WHERE id_receiver_user = u.id OR id_sender_user = u.id) as last_compliment_activity,
  (SELECT MAX(created_at) FROM redeem WHERE id_user = u.id) as last_redemption,
  -- Notification count
  (SELECT COUNT(*) FROM notifications WHERE user_id = u.id AND read = false) as unread_notifications
FROM "user" u
WHERE u.active = true;

-- Reward catalog view with enriched data
CREATE VIEW "v_reward_catalog" AS
SELECT 
  r.id,
  r.title,
  r.description,
  r.image,
  r.price,
  r.active,
  r.brand,
  r.redeemed,
  r.limited,
  r.slug,
  r.created_at,
  rc.name as category_name,
  rc.slug as category_slug,
  rc.color as category_color,
  -- Stock info
  COALESCE(SUM(s.quantity), 0) as total_stock,
  -- Popularity metrics
  (SELECT COUNT(*) FROM reward_wishlist WHERE reward_id = r.id) as wishlist_count,
  (SELECT AVG(CASE WHEN created_at >= NOW() - INTERVAL '30 days' THEN 1 ELSE 0 END)) as recent_popularity
FROM "rewards" r
LEFT JOIN "reward_categories" rc ON r.category_id = rc.id
LEFT JOIN "stock" s ON r.id = s.id_reward
WHERE r.active = true
GROUP BY r.id, rc.name, rc.slug, rc.color;

