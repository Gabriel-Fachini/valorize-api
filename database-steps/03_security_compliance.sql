-- Function for automatic audit logging
CREATE OR REPLACE FUNCTION log_data_changes()
RETURNS TRIGGER AS $$
DECLARE
  audit_user_id INT;
  audit_admin_id INT;
BEGIN
  -- Extract user IDs from the record
  audit_user_id := CASE 
    WHEN TG_OP = 'DELETE' THEN 
      CASE WHEN OLD ? 'id' THEN (OLD->>'id')::INT ELSE NULL END
    ELSE 
      CASE WHEN NEW ? 'id' THEN (NEW->>'id')::INT ELSE NULL END
  END;

  -- Determine if this is an admin action (you'll need to implement session context)
  audit_admin_id := current_setting('app.current_admin_user_id', true)::INT;

  INSERT INTO "audit_logs" (
    "user_id",
    "admin_user_id",
    "action", 
    "entity_type",
    "entity_id",
    "old_values",
    "new_values",
    "ip_address",
    "user_agent",
    "session_id",
    "compliance_flag",
    "timestamp"
  ) VALUES (
    audit_user_id,
    audit_admin_id,
    TG_OP,
    TG_TABLE_NAME,
    CASE WHEN TG_OP = 'DELETE' THEN OLD->>'id' ELSE NEW->>'id' END,
    CASE WHEN TG_OP IN ('UPDATE', 'DELETE') THEN row_to_json(OLD) ELSE NULL END,
    CASE WHEN TG_OP IN ('INSERT', 'UPDATE') THEN row_to_json(NEW) ELSE NULL END,
    inet(current_setting('app.current_ip_address', true)),
    current_setting('app.current_user_agent', true),
    current_setting('app.current_session_id', true),
    true, -- Mark as compliance-relevant
    NOW()
  );
  
  RETURN CASE WHEN TG_OP = 'DELETE' THEN OLD ELSE NEW END;
EXCEPTION
  WHEN OTHERS THEN
    -- Log audit failure but don't fail the original operation
    RAISE WARNING 'Audit logging failed: %', SQLERRM;
    RETURN CASE WHEN TG_OP = 'DELETE' THEN OLD ELSE NEW END;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Apply audit triggers to critical tables
