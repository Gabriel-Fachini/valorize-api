-- Function to check if user is locked out
CREATE OR REPLACE FUNCTION is_user_locked(user_email VARCHAR)
RETURNS BOOLEAN AS $$
DECLARE
  lock_until TIMESTAMPTZ;
BEGIN
  SELECT us.locked_until INTO lock_until
  FROM "user" u
  JOIN "user_security" us ON u.id = us.id_user
  WHERE u.email = user_email AND u.active = true;
  
  RETURN (lock_until IS NOT NULL AND lock_until > NOW());
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to record login attempt
CREATE OR REPLACE FUNCTION record_login_attempt(
  user_email VARCHAR,
  success BOOLEAN,
  ip_addr INET,
  user_agent_str TEXT
)
RETURNS VOID AS $$
DECLARE
  user_id_var INT;
  current_attempts INT;
BEGIN
  -- Get user ID
  SELECT u.id INTO user_id_var
  FROM "user" u
  WHERE u.email = user_email AND u.active = true;
  
  IF user_id_var IS NULL THEN
    RETURN; -- User not found
  END IF;
  
  IF success THEN
    -- Reset failed attempts on successful login
    UPDATE "user_security" 
    SET 
      "failed_login_attempts" = 0,
      "locked_until" = NULL,
      "last_login_at" = NOW(),
      "last_login_ip" = ip_addr,
      "last_login_user_agent" = user_agent_str
    WHERE "id_user" = user_id_var;
  ELSE
    -- Increment failed attempts
    UPDATE "user_security" 
    SET 
      "failed_login_attempts" = "failed_login_attempts" + 1,
      "locked_until" = CASE 
        WHEN "failed_login_attempts" + 1 >= 5 THEN NOW() + INTERVAL '30 minutes'
        ELSE NULL
      END
    WHERE "id_user" = user_id_var
    RETURNING "failed_login_attempts" INTO current_attempts;
    
    -- Create security event for failed login
    INSERT INTO "security_events" (
      "event_type", "severity", "user_id", "ip_address", "user_agent", 
      "description", "event_data"
    ) VALUES (
      'FAILED_LOGIN',
      CASE WHEN current_attempts >= 5 THEN 'HIGH' ELSE 'MEDIUM' END,
      user_id_var,
      ip_addr,
      user_agent_str,
      format('Failed login attempt #%s for user %s', current_attempts, user_email),
      jsonb_build_object('attempt_count', current_attempts, 'locked', current_attempts >= 5)
    );
  END IF;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

