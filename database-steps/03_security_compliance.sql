-- Active security threats view
CREATE VIEW "v_security_threats" AS
SELECT 
  se.event_type,
  se.severity,
  COUNT(*) as event_count,
  COUNT(DISTINCT se.user_id) as affected_users,
  COUNT(DISTINCT se.ip_address) as unique_ips,
  MAX(se.created_at) as latest_event,
  COUNT(CASE WHEN se.resolved = false THEN 1 END) as unresolved_count
FROM "security_events" se
WHERE se.created_at >= NOW() - INTERVAL '24 hours'
GROUP BY se.event_type, se.severity
ORDER BY 
  CASE se.severity 
    WHEN 'CRITICAL' THEN 1 
    WHEN 'HIGH' THEN 2 
    WHEN 'MEDIUM' THEN 3 
    ELSE 4 
  END, event_count DESC;

-- User security status view
CREATE VIEW "v_user_security_status" AS
SELECT 
  u.id,
  u.email,
  u.name,
  us.mfa_enabled,
  us.email_verified_at IS NOT NULL as email_verified,
  us.failed_login_attempts,
  us.locked_until IS NOT NULL AND us.locked_until > NOW() as currently_locked,
  us.last_login_at,
  (
    SELECT COUNT(*) 
    FROM user_sessions sess 
    WHERE sess.user_id = u.id 
      AND sess.revoked_at IS NULL 
      AND sess.expires_at > NOW()
  ) as active_sessions,
  (
    SELECT COUNT(*) 
    FROM security_events se 
    WHERE se.user_id = u.id 
      AND se.created_at >= NOW() - INTERVAL '30 days'
  ) as recent_security_events
FROM "user" u
LEFT JOIN "user_security" us ON u.id = us.id_user
WHERE u.active = true AND u.deleted_at IS NULL;

