-- I18n implementation validation
SELECT 
  'Internationalization Setup' as check_type,
  COUNT(DISTINCT l.code) as supported_languages,
  COUNT(DISTINCT c.code) as supported_currencies,
  COUNT(DISTINCT st.translation_key) as translation_keys,
  COUNT(DISTINCT ii.initiative_id) as localized_initiatives,
  COUNT(DISTINCT ri.reward_id) as localized_rewards
FROM languages l
CROSS JOIN currencies c
LEFT JOIN system_translations st ON l.code = st.language_code
LEFT JOIN initiatives_i18n ii ON l.code = ii.language_code
LEFT JOIN rewards_i18n ri ON l.code = ri.language_code
WHERE l.active = true AND c.active = true;

-- Localization coverage check
SELECT 
  l.code as language_code,
  l.name as language_name,
  COUNT(DISTINCT st.translation_key) as system_translations,
  COUNT(DISTINCT cvi.value_id) as company_values,
  COUNT(DISTINCT ii.initiative_id) as initiatives,
  COUNT(DISTINCT ri.reward_id) as rewards
FROM languages l
LEFT JOIN system_translations st ON l.code = st.language_code
LEFT JOIN company_values_i18n cvi ON l.code = cvi.language_code
LEFT JOIN initiatives_i18n ii ON l.code = ii.language_code
LEFT JOIN rewards_i18n ri ON l.code = ri.language_code
WHERE l.active = true
GROUP BY l.code, l.name
ORDER BY l.code;

