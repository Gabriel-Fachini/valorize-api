-- Supported languages and regional settings
CREATE TABLE "languages" (
  "code" varchar(5) PRIMARY KEY, -- ISO 639-1 + optional country (e.g., 'en', 'pt-BR', 'zh-CN')
  "name" varchar(100) NOT NULL, -- English name
  "native_name" varchar(100) NOT NULL, -- Native language name
  "active" boolean NOT NULL DEFAULT true,
  "rtl" boolean NOT NULL DEFAULT false, -- Right-to-left languages (Arabic, Hebrew)
  "flag_emoji" varchar(10), -- Country flag emoji for UI
  "decimal_separator" varchar(1) NOT NULL DEFAULT '.',
  "thousands_separator" varchar(1) NOT NULL DEFAULT ',',
  "date_format" varchar(20) NOT NULL DEFAULT 'YYYY-MM-DD',
  "time_format" varchar(10) NOT NULL DEFAULT '24h',
  "first_day_of_week" smallint NOT NULL DEFAULT 0, -- 0=Sunday, 1=Monday
  "created_at" timestamptz NOT NULL DEFAULT NOW(),
  "updated_at" timestamptz NOT NULL DEFAULT NOW()
