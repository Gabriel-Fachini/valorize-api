-- Function to get user's localized timestamp
CREATE OR REPLACE FUNCTION get_user_timestamp(
  user_id_param INT,
  timestamp_utc TIMESTAMPTZ
)
RETURNS TIMESTAMPTZ AS $$
DECLARE
  user_timezone VARCHAR(50);
BEGIN
  -- Get user's timezone preference
  SELECT timezone INTO user_timezone
  FROM user_preferences 
  WHERE user_id = user_id_param;
  
  -- Default to UTC if no preference found
  IF user_timezone IS NULL THEN
    user_timezone := 'UTC';
  END IF;
  
  -- Convert timestamp to user's timezone
  RETURN timezone(user_timezone, timestamp_utc);
END;
$$ LANGUAGE plpgsql STABLE;

-- Function to format currency amount based on user preference
CREATE OR REPLACE FUNCTION format_currency_amount(
  user_id_param INT,
  amount DECIMAL,
  currency_code_param VARCHAR(3) DEFAULT NULL
)
RETURNS TEXT AS $$
DECLARE
  user_currency VARCHAR(3);
  currency_symbol VARCHAR(10);
  symbol_position VARCHAR(10);
  decimal_places SMALLINT;
  formatted_amount TEXT;
BEGIN
  -- Get user's currency preference if not specified
  IF currency_code_param IS NULL THEN
    SELECT currency_code INTO user_currency
    FROM user_preferences 
    WHERE user_id = user_id_param;
    
    IF user_currency IS NULL THEN
      user_currency := 'USD';
    END IF;
  ELSE
    user_currency := currency_code_param;
  END IF;
  
  -- Get currency formatting info
  SELECT symbol, symbol_position, decimal_places
  INTO currency_symbol, symbol_position, decimal_places
  FROM currencies 
  WHERE code = user_currency;
  
  -- Format the amount
  formatted_amount := to_char(amount, 'FM999,999,999,990.' || repeat('0', decimal_places));
  
  -- Apply currency symbol
  IF symbol_position = 'before' THEN
    RETURN currency_symbol || formatted_amount;
  ELSE
    RETURN formatted_amount || currency_symbol;
  END IF;
END;
$$ LANGUAGE plpgsql STABLE;

