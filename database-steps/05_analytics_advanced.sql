-- Pre-calculated dashboard data for performance
CREATE MATERIALIZED VIEW "mv_user_dashboard_stats" AS
SELECT 
  u.id as user_id,
  -- Basic info
  u.name,
  u.email,
  u.coins_to_donate,
  u.coins_to_spend,
  -- Compliment stats
  COALESCE(compliment_stats.sent, 0) as compliments_sent,
  COALESCE(compliment_stats.received, 0) as compliments_received,
  COALESCE(compliment_stats.coins_received, 0) as total_coins_received,
  -- Recent activity
  COALESCE(recent_activity.last_compliment, u.created_at) as last_compliment_activity,
  COALESCE(recent_activity.last_login, u.created_at) as last_login,
  -- Rankings
  ROW_NUMBER() OVER (ORDER BY COALESCE(compliment_stats.coins_received, 0) DESC) as coins_received_rank,
  ROW_NUMBER() OVER (ORDER BY COALESCE(compliment_stats.sent, 0) DESC) as compliments_sent_rank,
  -- Engagement score (calculated based on various factors)
  (
    (COALESCE(compliment_stats.sent, 0) * 2) + 
    (COALESCE(compliment_stats.received, 0) * 3) + 
    (COALESCE(reward_stats.claimed, 0) * 1)
  ) as engagement_score
FROM "user" u
LEFT JOIN (
  SELECT 
    id_receiver_user,
    COUNT(*) as received,
    SUM(quantity) as coins_received
  FROM compliment 
  WHERE created_at >= NOW() - INTERVAL '30 days'
  GROUP BY id_receiver_user
) compliment_received ON u.id = compliment_received.id_receiver_user
LEFT JOIN (
  SELECT 
    id_sender_user,
    COUNT(*) as sent
  FROM compliment 
  WHERE created_at >= NOW() - INTERVAL '30 days'
  GROUP BY id_sender_user
) compliment_sent ON u.id = compliment_sent.id_sender_user
LEFT JOIN (
  SELECT 
    id_receiver_user,
    compliment_sent.sent,
    compliment_received.received,
    compliment_received.coins_received
  FROM compliment_received
  FULL OUTER JOIN compliment_sent ON compliment_received.id_receiver_user = compliment_sent.id_sender_user
) compliment_stats ON u.id = compliment_stats.id_receiver_user
LEFT JOIN (
  SELECT 
    id_user,
    COUNT(*) as claimed
  FROM redeem 
  WHERE created_at >= NOW() - INTERVAL '30 days'
  GROUP BY id_user
) reward_stats ON u.id = reward_stats.id_user
LEFT JOIN (
  SELECT 
    user_id,
    MAX(CASE WHEN activity_type LIKE '%COMPLIMENT%' THEN timestamp END) as last_compliment,
    MAX(CASE WHEN activity_type = 'LOGIN' THEN timestamp END) as last_login
  FROM user_activities
  WHERE timestamp >= NOW() - INTERVAL '90 days'
  GROUP BY user_id
) recent_activity ON u.id = recent_activity.user_id
WHERE u.active = true AND u.deleted_at IS NULL;

-- Indexes for materialized view
CREATE INDEX "idx_mv_user_dashboard_stats_user" ON "mv_user_dashboard_stats" ("user_id");
CREATE INDEX "idx_mv_user_dashboard_stats_engagement" ON "mv_user_dashboard_stats" ("engagement_score" DESC);
CREATE INDEX "idx_mv_user_dashboard_stats_coins_rank" ON "mv_user_dashboard_stats" ("coins_received_rank");

-- Company-wide analytics materialized view
CREATE MATERIALIZED VIEW "mv_company_analytics" AS
SELECT 
  DATE_TRUNC('month', created_at) as month,
  -- User metrics
  COUNT(DISTINCT CASE WHEN entity_type = 'user' THEN entity_id END) as active_users,
  -- Engagement metrics
  COUNT(CASE WHEN activity_type LIKE '%COMPLIMENT%' THEN 1 END) as compliment_activities,
  COUNT(CASE WHEN activity_type LIKE '%REWARD%' THEN 1 END) as reward_activities,
  COUNT(CASE WHEN activity_type = 'LOGIN' THEN 1 END) as logins,
  -- Value metrics
  AVG(CASE WHEN activity_type = 'COMPLIMENT_SENT' THEN value_impact END) as avg_coins_per_compliment,
  SUM(CASE WHEN activity_type = 'REWARD_CLAIMED' THEN value_impact END) as total_coins_spent,
  -- Trend indicators
  LAG(COUNT(CASE WHEN activity_type = 'LOGIN' THEN 1 END)) OVER (ORDER BY DATE_TRUNC('month', created_at)) as prev_month_logins
FROM user_activities
WHERE created_at >= NOW() - INTERVAL '12 months'
GROUP BY DATE_TRUNC('month', created_at)
ORDER BY month;

