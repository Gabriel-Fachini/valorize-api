-- Comprehensive user engagement report
CREATE VIEW "v_user_engagement_report" AS
SELECT 
  u.id,
  u.name,
  u.email,
  up.language_code,
  up.timezone,
  us.last_login_at,
  -- Engagement metrics (last 30 days)
  ua_stats.login_count,
  ua_stats.session_count,
  ua_stats.avg_session_duration,
  ua_stats.compliment_activities,
  ua_stats.reward_activities,
  ua_stats.social_activities,
  -- Behavioral indicators
  CASE 
    WHEN us.last_login_at >= NOW() - INTERVAL '7 days' THEN 'Active'
    WHEN us.last_login_at >= NOW() - INTERVAL '30 days' THEN 'Inactive'
    ELSE 'Dormant'
  END as user_status,
  -- Engagement level scoring
  CASE 
    WHEN ua_stats.engagement_score >= 100 THEN 'High'
    WHEN ua_stats.engagement_score >= 50 THEN 'Medium'
    WHEN ua_stats.engagement_score >= 10 THEN 'Low'
    ELSE 'Very Low'
  END as engagement_level
FROM "user" u
LEFT JOIN "user_preferences" up ON u.id = up.user_id
LEFT JOIN "user_security" us ON u.id = us.id_user
LEFT JOIN (
  SELECT 
    user_id,
    COUNT(CASE WHEN activity_type = 'LOGIN' THEN 1 END) as login_count,
    COUNT(DISTINCT session_id) as session_count,
    AVG(EXTRACT(EPOCH FROM (
      LEAD(timestamp) OVER (PARTITION BY session_id ORDER BY timestamp) - timestamp
    ))) as avg_session_duration,
    COUNT(CASE WHEN activity_type LIKE '%COMPLIMENT%' THEN 1 END) as compliment_activities,
    COUNT(CASE WHEN activity_type LIKE '%REWARD%' THEN 1 END) as reward_activities,
    COUNT(CASE WHEN activity_type IN ('REACTION_GIVEN', 'COMMENT_MADE') THEN 1 END) as social_activities,
    (
      COUNT(CASE WHEN activity_type LIKE '%COMPLIMENT%' THEN 1 END) * 3 +
      COUNT(CASE WHEN activity_type LIKE '%REWARD%' THEN 1 END) * 2 +
      COUNT(CASE WHEN activity_type = 'LOGIN' THEN 1 END) * 1
    ) as engagement_score
  FROM user_activities
  WHERE timestamp >= NOW() - INTERVAL '30 days'
  GROUP BY user_id
) ua_stats ON u.id = ua_stats.user_id
WHERE u.active = true AND u.deleted_at IS NULL;

-- Company values impact analysis
CREATE VIEW "v_company_values_impact" AS
SELECT 
  cv.code,
  cv.name,
  cvi.language_code,
  COALESCE(cvi.name, cv.name) as localized_name,
  -- Usage statistics
  COUNT(c.id) as total_compliments,
  COUNT(DISTINCT c.id_sender_user) as unique_senders,
  COUNT(DISTINCT c.id_receiver_user) as unique_receivers,
  SUM(c.quantity) as total_coins,
  AVG(c.quantity) as avg_coins_per_compliment,
  -- Time-based analysis
  COUNT(CASE WHEN c.created_at >= NOW() - INTERVAL '30 days' THEN 1 END) as recent_compliments,
  COUNT(CASE WHEN c.created_at >= NOW() - INTERVAL '7 days' THEN 1 END) as weekly_compliments,
  -- Engagement metrics
  COUNT(cr.id) as total_reactions,
  COUNT(cc.id) as total_comments,
  -- Trend calculation
  (
    COUNT(CASE WHEN c.created_at >= NOW() - INTERVAL '30 days' THEN 1 END)::FLOAT /
    NULLIF(COUNT(CASE WHEN c.created_at >= NOW() - INTERVAL '60 days' 
                      AND c.created_at < NOW() - INTERVAL '30 days' THEN 1 END), 0)
  ) as monthly_growth_rate
FROM company_values cv
LEFT JOIN company_values_i18n cvi ON cv.id = cvi.value_id
LEFT JOIN compliment c ON cv.id = c.company_value_id
LEFT JOIN compliment_reactions cr ON c.id = cr.compliment_id
LEFT JOIN compliment_comments cc ON c.id = cc.compliment_id
WHERE cv.active = true
GROUP BY cv.code, cv.name, cvi.language_code, cvi.name
ORDER BY total_compliments DESC;

-- Reward performance analytics
CREATE VIEW "v_reward_performance" AS
SELECT 
  r.id,
  r.title,
  r.slug,
  rc.name as category,
  r.price,
  r.redeemed,
  r.limited,
  -- Performance metrics
  (r.redeemed::FLOAT / NULLIF(GREATEST(stock_total.total_stock, r.redeemed), 0)) * 100 as redemption_rate,
  wishlist_stats.wishlist_count,
  search_stats.search_appearances,
  -- Financial impact
  (r.price * r.redeemed) as total_revenue,
  rp.coins_price * r.redeemed as total_coins_spent,
  -- Popularity metrics
  ROW_NUMBER() OVER (ORDER BY r.redeemed DESC) as popularity_rank,
  ROW_NUMBER() OVER (PARTITION BY rc.name ORDER BY r.redeemed DESC) as category_rank,
  -- Availability status
  CASE 
    WHEN r.limited AND COALESCE(stock_total.total_stock, 0) = 0 THEN 'Out of Stock'
    WHEN r.limited AND COALESCE(stock_total.total_stock, 0) <= 10 THEN 'Low Stock'
    WHEN r.active THEN 'Available'
    ELSE 'Inactive'
  END as availability_status
FROM rewards r
LEFT JOIN reward_categories rc ON r.category_id = rc.id
LEFT JOIN reward_pricing rp ON r.id = rp.reward_id
LEFT JOIN (
  SELECT id_reward, SUM(quantity) as total_stock
  FROM stock
  GROUP BY id_reward
) stock_total ON r.id = stock_total.id_reward
LEFT JOIN (
  SELECT reward_id, COUNT(*) as wishlist_count
  FROM reward_wishlist
  GROUP BY reward_id
) wishlist_stats ON r.id = wishlist_stats.reward_id
LEFT JOIN (
  SELECT 
    clicked_result_id,
    COUNT(*) as search_appearances
  FROM search_queries
  WHERE clicked_result_type = 'REWARD'
  GROUP BY clicked_result_id
) search_stats ON r.id = search_stats.clicked_result_id
WHERE r.deleted_at IS NULL;

