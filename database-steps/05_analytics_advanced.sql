-- Function to clean up old data based on retention policies
CREATE OR REPLACE FUNCTION cleanup_old_data()
RETURNS VOID AS $$
DECLARE
  policy RECORD;
  deleted_count INT;
BEGIN
  FOR policy IN SELECT * FROM data_retention_policies WHERE active = true LOOP
    CASE policy.deletion_strategy
      WHEN 'SOFT' THEN
        -- Soft delete by setting deleted_at
        EXECUTE format('UPDATE %I SET deleted_at = NOW() WHERE created_at < NOW() - INTERVAL ''%s days'' AND deleted_at IS NULL', 
          policy.table_name, policy.retention_days);
        GET DIAGNOSTICS deleted_count = ROW_COUNT;
        
      WHEN 'HARD' THEN
        -- Hard delete records
        EXECUTE format('DELETE FROM %I WHERE created_at < NOW() - INTERVAL ''%s days''', 
          policy.table_name, policy.retention_days);
        GET DIAGNOSTICS deleted_count = ROW_COUNT;
        
      WHEN 'ARCHIVE' THEN
        -- For now, just log that archiving is needed
        INSERT INTO system_health_checks (check_name, check_category, status, message)
        VALUES ('data_archival', 'DATA_INTEGRITY', 'WARNING', 
                format('Table %s needs archival of records older than %s days', 
                       policy.table_name, policy.retention_days));
        CONTINUE;
    END CASE;
    
    -- Update policy last cleanup time
    UPDATE data_retention_policies 
    SET last_cleanup = NOW() 
    WHERE id = policy.id;
    
    -- Log cleanup activity
    INSERT INTO system_health_checks (check_name, check_category, status, message)
    VALUES ('data_cleanup', 'DATA_INTEGRITY', 'PASS', 
            format('Cleaned up %s records from %s using %s strategy', 
                   deleted_count, policy.table_name, policy.deletion_strategy));
  END LOOP;
END;
$$ LANGUAGE plpgsql;

