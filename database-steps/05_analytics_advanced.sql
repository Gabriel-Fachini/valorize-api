-- Function to calculate user engagement score
CREATE OR REPLACE FUNCTION calculate_user_engagement_score(user_id_param INT)
RETURNS DECIMAL AS $$
DECLARE
  score DECIMAL := 0;
  login_count INT;
  compliment_count INT;
  reward_count INT;
  social_count INT;
BEGIN
  -- Get activity counts for the last 30 days
  SELECT 
    COUNT(CASE WHEN activity_type = 'LOGIN' THEN 1 END),
    COUNT(CASE WHEN activity_type LIKE '%COMPLIMENT%' THEN 1 END),
    COUNT(CASE WHEN activity_type LIKE '%REWARD%' THEN 1 END),
    COUNT(CASE WHEN activity_type IN ('REACTION_GIVEN', 'COMMENT_MADE') THEN 1 END)
  INTO login_count, compliment_count, reward_count, social_count
  FROM user_activities
  WHERE user_id = user_id_param 
    AND timestamp >= NOW() - INTERVAL '30 days';
  
  -- Calculate weighted score
  score := (login_count * 1) + (compliment_count * 3) + (reward_count * 2) + (social_count * 1.5);
  
  RETURN score;
END;
$$ LANGUAGE plpgsql STABLE;

-- Function to update search indexes
CREATE OR REPLACE FUNCTION update_search_indexes()
RETURNS VOID AS $$
BEGIN
  -- Update reward search indexes
  INSERT INTO search_indexes (entity_type, entity_id, language_code, search_vector, content_hash)
  SELECT 
    'REWARD',
    r.id,
    COALESCE(ri.language_code, 'en'),
    to_tsvector('english', COALESCE(ri.title, r.title) || ' ' || COALESCE(ri.description, r.description)),
    md5(COALESCE(ri.title, r.title) || COALESCE(ri.description, r.description))
  FROM rewards r
  LEFT JOIN rewards_i18n ri ON r.id = ri.reward_id
  WHERE r.active = true AND r.deleted_at IS NULL
  ON CONFLICT (entity_type, entity_id, language_code) 
  DO UPDATE SET 
    search_vector = EXCLUDED.search_vector,
    content_hash = EXCLUDED.content_hash,
    indexed_at = NOW()
  WHERE search_indexes.content_hash != EXCLUDED.content_hash;
  
  -- Update initiative search indexes
  INSERT INTO search_indexes (entity_type, entity_id, language_code, search_vector, content_hash)
  SELECT 
    'INITIATIVE',
    i.id,
    COALESCE(ii.language_code, 'en'),
    to_tsvector('english', COALESCE(ii.title, i.title) || ' ' || COALESCE(ii.description, i.description)),
    md5(COALESCE(ii.title, i.title) || COALESCE(ii.description, i.description))
  FROM initiatives i
  LEFT JOIN initiatives_i18n ii ON i.id = ii.initiative_id
  WHERE i.active = true AND i.deleted_at IS NULL
  ON CONFLICT (entity_type, entity_id, language_code) 
  DO UPDATE SET 
    search_vector = EXCLUDED.search_vector,
    content_hash = EXCLUDED.content_hash,
    indexed_at = NOW()
  WHERE search_indexes.content_hash != EXCLUDED.content_hash;
END;
$$ LANGUAGE plpgsql;

