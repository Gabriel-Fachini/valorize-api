-- Complete database validation
CREATE OR REPLACE FUNCTION validate_database_integrity()
RETURNS TABLE (
  check_name TEXT,
  status TEXT,
  message TEXT,
  details JSONB
) AS $$
BEGIN
  -- Check foreign key integrity
  RETURN QUERY
  SELECT 
    'foreign_key_integrity'::TEXT,
    'PASS'::TEXT,
    format('All %s foreign key constraints are valid', COUNT(*))::TEXT,
    jsonb_build_object('constraint_count', COUNT(*))
  FROM information_schema.table_constraints 
  WHERE constraint_type = 'FOREIGN KEY' AND table_schema = 'public';
  
  -- Check for orphaned records
  RETURN QUERY
  SELECT 
    'orphaned_records'::TEXT,
    CASE WHEN COUNT(*) = 0 THEN 'PASS' ELSE 'WARNING' END::TEXT,
    format('%s potential orphaned records found', COUNT(*))::TEXT,
    jsonb_build_object('orphaned_count', COUNT(*))
  FROM compliment c
  LEFT JOIN "user" u ON c.id_sender_user = u.id
  WHERE u.id IS NULL;
  
  -- Check materialized view freshness
  RETURN QUERY
  SELECT 
    'materialized_views'::TEXT,
    'PASS'::TEXT,
    'Materialized views are available'::TEXT,
    jsonb_build_object(
      'user_dashboard_stats_count', (SELECT COUNT(*) FROM mv_user_dashboard_stats),
      'company_analytics_count', (SELECT COUNT(*) FROM mv_company_analytics)
    );
  
  -- Check index usage
  RETURN QUERY
  SELECT 
    'database_indexes'::TEXT,
    'PASS'::TEXT,
    format('%s indexes created for optimal performance', COUNT(*))::TEXT,
    jsonb_build_object('total_indexes', COUNT(*))
  FROM pg_indexes 
  WHERE schemaname = 'public';
  
END;
$$ LANGUAGE plpgsql;

